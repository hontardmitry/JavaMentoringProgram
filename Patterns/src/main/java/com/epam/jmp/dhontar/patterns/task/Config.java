package com.epam.jmp.dhontar.patterns.task;

import java.util.Map;

/**
 * The type Config.
 */
public class Config {
    private Map<String, String> users;

    /**
     * Gets users.
     *
     * @return the users
     */
    public Map<String, String> getUsers() {
        return users;
    }

    /**
     * Sets users.
     *
     * @param users the users
     */
    public void setUsers(Map<String, String> users) {
        this.users = users;
    }
}
